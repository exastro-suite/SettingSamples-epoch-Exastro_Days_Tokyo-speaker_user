package exastro.Exastro_Days_Tokyo.speaker_user.controller.api.v1.form;

public class SpeakerDetailForm {

	private int speakerId;
	private String speakerName;
	private String speakerProfile;
	private boolean isDeleted;

	public int getSpeakerId() {
		return speakerId;
	}
	public void setSpeakerId(int speakerId) {
		this.speakerId = speakerId;
	}
	
	public String getSpeakerName() {
		return speakerName;
	}
	public void setSpeakerName(String speakerName) {
		this.speakerName = speakerName;
	}
	
	public String getSpeakerProfile() {
		return speakerProfile;
	}
	public void setSpeakerProfile(String speakerProfile) {
		this.speakerProfile = speakerProfile;
	}
	
	public boolean isDeleted() {
		return isDeleted;
	}
	public void setDeleted(boolean isDeleted) {
		this.isDeleted = isDeleted;
	}
}

